import math

## Factorial and Square root
print(math.factorial(3))
print(math.sqrt(64))

## greatest common denominator GCD
print(math.gcd(52, 8)) # gosagu
print(math.gcd(24, 36, 82))


## Degrees and radians
## degree to radian
print(math.radians(360)) # 2pi radin = 360 degree
print(math.pi * 2)

print(math.degrees(math.pi * 2)) # it will give 360 degree back
